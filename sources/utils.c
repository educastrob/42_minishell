#include "lexing.h"

int		is_whitespace(char c)
{
	return (c == ' ' || c == '\t' || c == '\n');
}
int		is_metacharacter(char c)
{
	if (c == '|' || c == '<' || c == '>' || c == '&' || c == '\'' || c == '\"' || c == '(' || c == ')' || is_whitespace(c))
		return (1);
	return (0);
}